'''
========================================
[유형 3] 탐욕법 (Greedy)
========================================
빈출 이유: "최소/최대를 구하라" 문제의 핵심
핵심 패턴: 정렬 후 국소 최적 선택 → 전체 최적

----------------------------------------
[문제] 구명보트 (프로그래머스 Lv.2)
----------------------------------------
무인도에 사람들이 갇혀있어 구명보트로 탈출해야 합니다.
구명보트는 최대 2명이 탈 수 있고, 무게 제한이 limit입니다.
모든 사람을 구출하기 위해 필요한 보트 수의 최솟값을 구하세요.

예: people=[70,50,80,50], limit=100
  → [50,50] [80] [70] 이렇게 보트 3개 필요

입출력 예:
people          | limit | return
[70,50,80,50]   |  100  |   3
[70,80,50]      |  100  |   3

----------------------------------------
[내 풀이 시도 공간]
----------------------------------------
def solution(people, limit):
    pass

----------------------------------------
[해설 & 정답]
----------------------------------------
핵심 아이디어:
  가장 가벼운 사람 + 가장 무거운 사람을 같이 태우려 시도
  태울 수 없으면 무거운 사람 혼자 → 투 포인터

  정렬 후 left(가벼운), right(무거운) 포인터
  합 ≤ limit → 둘 다 태움, left++, right--, boats+1
  합 > limit  → 무거운 사람만 태움, right--, boats+1
'''

def solution(people, limit):
    people.sort()
    left, right = 0, len(people) - 1
    boats = 0

    while left <= right:
        if people[left] + people[right] <= limit:
            left += 1      # 가벼운 사람도 같이 탑승
        right -= 1         # 무거운 사람은 항상 탑승
        boats += 1

    return boats

'''
왜 이 그리디가 최적인가?
  - 무거운 사람은 반드시 태워야 함
  - 그 보트에 가장 가벼운 사람을 함께 태우는 것이 최선
  - 더 무거운 사람과 쌍을 지으면 보트 낭비 없이 같거나 더 나쁨

오답 패턴 주의:
  ❌ 두 명씩 짝지어 탐색 (인접한 두 명 합산) → 최적 아님
  ✓ 항상 가장 가벼운 vs 가장 무거운 비교

========================================
[심화] 섬 연결하기 (프로그래머스 Lv.3) — MST Greedy
========================================
n개의 섬을 최소 비용으로 연결하는 다리 비용 합 반환
→ 최소 신장 트리(MST): 크루스칼 알고리즘

def solution_mst(n, costs):
    costs.sort(key=lambda x: x[2])   # 비용 기준 정렬
    parent = list(range(n))

    def find(x):                      # Union-Find
        if parent[x] != x:
            parent[x] = find(parent[x])
        return parent[x]

    def union(a, b):
        a, b = find(a), find(b)
        if a == b: return False
        parent[b] = a
        return True

    total = 0
    for a, b, cost in costs:
        if union(a, b):
            total += cost
    return total

========================================
핵심 정리
========================================
그리디 문제 풀이 순서:
  1. "무엇을 기준으로 정렬할까?" 결정
  2. 정렬된 상태에서 국소 최적 선택
  3. 선택이 전체 최적임을 직관적으로 증명

자주 쓰는 패턴:
  - 투 포인터: 양쪽에서 조이기 (구명보트, 두 수의 합)
  - 우선순위 큐: heapq로 항상 최솟값 선택
  - 정렬 기준 커스텀: sort(key=lambda x: (x[0], -x[1]))
'''

# 테스트
print(solution([70, 50, 80, 50], 100))  # 3
print(solution([70, 80, 50], 100))      # 3

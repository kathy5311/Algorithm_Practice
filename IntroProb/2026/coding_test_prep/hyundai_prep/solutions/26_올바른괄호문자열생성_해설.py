'''
[해설] 올바른 괄호 문자열 생성
========================================
핵심 아이디어:
  ( 를 추가할 수 있는 조건: 아직 열린 괄호를 더 쓸 수 있을 때 (open < n)
  ) 를 추가할 수 있는 조건: 닫지 않은 열린 괄호가 있을 때 (close < open)
  기저 조건: 문자열 길이가 2*n일 때 결과에 추가

  재귀 구조:
    dfs(현재문자열, open 개수, close 개수)
    종료 조건: len(cur) == 2*n
    두 갈래:
      ( 추가: open < n 일 때
      ) 추가: close < open 일 때
'''

def solution(n):
    result = []

    def dfs(cur, open_cnt, close_cnt):
        if len(cur) == 2 * n:
            result.append(cur)
            return
        if open_cnt < n:
            dfs(cur + '(', open_cnt + 1, close_cnt)
        if close_cnt < open_cnt:
            dfs(cur + ')', open_cnt, close_cnt + 1)

    dfs('', 0, 0)
    return result

'''
========================================
포인트 정리
========================================
1. 왜 close < open 조건인가?
   ) 는 짝이 되는 ( 가 앞에 있어야만 올바름
   → 닫힌 괄호 개수가 열린 괄호 개수를 넘으면 안 됨

2. 가지치기(pruning)가 핵심:
   조건 없이 모든 조합을 만들면 2^(2n)개 → 이후 유효성 검사
   조건을 걸면서 만들면 처음부터 올바른 문자열만 생성 → 훨씬 효율적

3. 카탈란 수 (Catalan Number):
   n쌍 괄호로 만들 수 있는 올바른 문자열의 수 = C(n)
   C(1)=1, C(2)=2, C(3)=5, C(4)=14, C(5)=42 ...
   n=7일 때 429개 → 충분히 완전 탐색 가능

4. 시간복잡도: O(C(n)) ≈ O(4^n / n^1.5)

응용:
  - 올바른 괄호 문자열 검증 → 스택 또는 카운터 방식 (05번 참고)
  - 여러 종류 괄호 ({[]}) → 스택 방식으로 검증
'''

# 테스트
print(solution(1))  # ['()']
print(solution(2))  # ['(())', '()()']
print(solution(3))  # ['((()))', '(()())', '(())()', '()(())', '()()()']

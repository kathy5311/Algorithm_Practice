'''
[해설] 폰켓몬
========================================
핵심 아이디어:
  선택 가능 마릿수 = N/2
  최대 종류 수     = len(set(nums))

  두 가지 제약이 동시에 적용:
    종류 수 < N/2 → 종류 수가 한계 (아무리 골라도 이 이상 안 됨)
    종류 수 > N/2 → N/2가 한계 (더 많이 고를 수 없음)
    종류 수 = N/2 → 딱 맞게 전부 다른 종류

  → min(종류 수, N//2)
'''

def solution(nums):
    return min(len(set(nums)), len(nums) // 2)

'''
========================================
포인트 정리
========================================
1. set(nums): 중복 제거 → 종류 수
2. len(nums) // 2: 선택 가능 마릿수

단 한 줄로 해결 가능 → Lv.1이지만 발상 자체가 핵심
  "두 제약 중 더 엄격한 것이 정답" 패턴

응용: "최대 k개 선택할 때 최대 종류 수"
  → min(len(set(arr)), k)
'''

# 테스트
print(solution([3, 1, 2, 3]))       # 2
print(solution([3, 3, 3, 2, 2, 4])) # 3
print(solution([3, 3, 3, 2, 2, 2])) # 2
